package day11_controlFlowStatements;

public class task47_writeStarPattern {

	public static void main(String[] args) {
		
		int rows=10;
		int columns=4;
		
		for(int i=1; i<=rows; i++) {
			
			for(int j=1; j<=columns; j++) {
				System.out.print("*");
			
		}		
		
		System.out.println();

	}

}
}