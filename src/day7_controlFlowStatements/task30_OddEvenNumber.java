package day7_controlFlowStatements;

public class task30_OddEvenNumber {

	public static void main(String[] args) {
		// Write a java program: Declare and initialize a number
		//						 Display whether the number is an odd number or even number
		
		int x;
		x=35;
		if( x % 2 == 0 ) {
			System.out.println("\"" + x + "\"" + " is an even number");
		}else {
		System.out.println("\"" + x + "\"" + " is an odd number");
	}

}
}